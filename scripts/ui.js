function showCredits() {
   // Show gray overlay
   $('#gray_overlay').css('display', 'block');

   // Show credits box
   $('#credits_overlay').css('display', 'block');
}

function closeCredits() {
   // Hide gray overlay
   $('#gray_overlay').css('display', 'none');

   // Hide credits box
   $('#credits_overlay').css('display', 'none');
}
